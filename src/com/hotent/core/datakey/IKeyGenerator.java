package com.hotent.core.datakey;

/**
 * 键产生器接口。
 * @author ray
 *
 */
public interface IKeyGenerator {
	/**
	 * 产生下一个ID。
	 * @return
	 * @throws Exception
	 */
	public Object nextId() throws Exception;
	
	/**
	 * 设置流水号别名。
	 * 只有流水号，其他的没有用到这个接口。
	 * @param alias
	 */
	public void setAlias(String alias);

}
